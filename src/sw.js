const CACHE_NAME = 'satx-tools-cache-v1';
const STATIC_ASSETS = [
  '/',
  '/about.html',
  '/privacy.html',
  '/terms.html',
  '/disclaimer.html',
  '/assets/css/main.css',
  '/assets/css/tools.css',
  '/assets/js/main.js',
  '/assets/images/logo.png',
  '/manifest.json'
];

// Install Event - Pre-cache critical static shell
self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME).then(cache => {
      console.log('[Service Worker] Pre-caching static shell');
      return cache.addAll(STATIC_ASSETS);
    }).then(() => self.skipWaiting())
  );
});

// Activate Event - Clean up old caches
self.addEventListener('activate', event => {
  event.waitUntil(
    caches.keys().then(keys => {
      return Promise.all(
        keys.map(key => {
          if (key !== CACHE_NAME) {
            console.log('[Service Worker] Removing old cache:', key);
            return caches.delete(key);
          }
        })
      );
    }).then(() => self.clients.claim())
  );
});

// Fetch Event - Stale-While-Revalidate strategy for static resources & runtime caching for dynamic scripts
self.addEventListener('fetch', event => {
  // Only handle HTTP/HTTPS (skip browser extensions, chrome-extension://, etc.)
  if (!event.request.url.startsWith(self.location.origin)) {
    return;
  }

  event.respondWith(
    caches.match(event.request).then(cachedResponse => {
      if (cachedResponse) {
        // Fetch new version in the background to update cache (Stale-While-Revalidate)
        fetch(event.request).then(networkResponse => {
          if (networkResponse.status === 200) {
            caches.open(CACHE_NAME).then(cache => cache.put(event.request, networkResponse));
          }
        }).catch(() => {/* Ignore network fail in background */});
        
        return cachedResponse;
      }

      // Fallback to network, cache it on the fly
      return fetch(event.request).then(networkResponse => {
        if (!networkResponse || networkResponse.status !== 200 || networkResponse.type !== 'basic') {
          return networkResponse;
        }

        const responseToCache = networkResponse.clone();
        caches.open(CACHE_NAME).then(cache => {
          cache.put(event.request, responseToCache);
        });

        return networkResponse;
      });
    })
  );
});
